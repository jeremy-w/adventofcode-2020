class Point (NamedTuple):
	east: int
	southeast: int
	
	def __mul__(self, other: int):
		return Point(self.east * other, self.southeast * other)
	
	def __add__(self, other):
		return Point(self.east + other.east, self.southeast + other.southeast)
	
	@staticmethod
	def from_dir(s):
		return MAP[s]

