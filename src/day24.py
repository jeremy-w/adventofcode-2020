def black_neighbors(around, counter) -> int:
	return len([n for n in neighbors(around) if is_black(counter[n])])

def should_flip(point, counter):
	nc = black_neighbors(point, counter)
	if is_black(counter[point]):
		if nc == 0 or nc > 2:
			return True
	elif nc == 2:
		return True
	return False

def with_frontier(next):
	ks = next.keys()
	all = set()
	for k in ks:
		all.add(k)
		for n in neighbors(k):
			all.add(n)
	return all

def step(counter):
	next = Counter(counter)
	keys = with_frontier(next)
	for p in keys:
		if should_flip(p, counter):
			next[p] += 1
	return next

def run(ndays, c):
	for i in range(ndays):
		c = step(c)
